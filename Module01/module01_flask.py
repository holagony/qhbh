import json
import simplejson
from flask import Blueprint, request, jsonify, current_app
from tasks.dispatcher_worker import celery_submit, celery_task_status
from Module01.pageA_handler import climate_features_stats
from Module01.pageB_handler import extreme_climate_features
from Module01.pageC_handler import grass_features_stats
